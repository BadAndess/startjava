package com.startjava.lesson_1.variable;

public class Variable {
    public static void main(String[] args) {
        byte core = 4;
        short ram = 8;
        int year = 2014;
        long serial = 575878578;
        float frq = 2.6f;
        double cost = 100.555;
        char user = '$';
        boolean win = true;

        System.out.println(core + " " + ram + " " + " " + year +
                " " + serial + " " + " " + frq + " " + cost + " " + user + " " + win);
    }
}
